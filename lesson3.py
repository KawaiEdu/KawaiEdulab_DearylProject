angka1 = float(input("masukan angka pertama "))
angka2 = float(input("masukan angka ke dua "))

tambah = angka1 + angka2
minus = angka1 - angka2
star = angka1 * angka2
pembagian = angka1 / angka2
modulus = angka1 % angka2
pangkat = angka1 ** angka2
pembagianbulat = angka1 // angka2

print("hasil penjulahan ", tambah)
print("hasil dari pengurangan ", minus)
print("hasil dari perkalian ", star)
print("hasil dari pembagian ", pembagian)
print("hasil dari modulus ", modulus)
print("hasil dari perpangkatan ", pangkat)
print("hasil dari pembagian bulat ", pembagianbulat)